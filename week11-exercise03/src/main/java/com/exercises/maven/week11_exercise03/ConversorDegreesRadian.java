package com.exercises.maven.week11_exercise03;

public class ConversorDegreesRadian {
		
	public double degreesToRadians(double value) {
		double result = Math.toRadians(value);
		return result;
	}
	public double radiansToDegrees(double value) {
		double result = Math.toDegrees(value);
		return result;
	}
}
		